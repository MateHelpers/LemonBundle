namespace Mate\LemonBundle\Helper;

class Helper
{
    public static function from_camel_to_snake($input) {
        preg_match_all('!([A-Z][A-Z0-9]*(?=$|[A-Z][a-z0-9])|[A-Za-z][a-z0-9]+)!', $input, $matches);
        $ret = $matches[0];
        foreach ($ret as &$match) {
            $match = $match == strtoupper($match) ? strtolower($match) : lcfirst($match);
        }
        return implode('_', $ret);
    }

    public static function to_camel_case($str, array $noStrip = [])
    {
        // non-alpha and non-numeric characters become spaces
        $str = preg_replace('/[^a-z0-9' . implode("", $noStrip) . ']+/i', ' ', $str);
        $str = trim($str);
        // uppercase the first character of each word
        $str = ucwords($str);
        $str = str_replace(" ", "", $str);
        $str = lcfirst($str);

        return $str;
    }

    public static function is_multidimensional(array $array) {
        return count($array) !== count($array, COUNT_RECURSIVE);
    }

    public static function ends_with($string, $char)
    {
        $length = strlen($char);
        if ($length == 0) {
            return true;
        }

        return (substr($string, -$length) === $char);
    }
}